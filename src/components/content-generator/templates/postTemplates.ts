const generateTemplates = (
  baseId: string,
  titles: string[],
  descriptions: string[],
  prompts: string[],
  icons: any[]
): PostTemplate[] => {
  return titles.map((title, index) => ({
    id: `${baseId}-${index + 1}`,
    title,
    description: descriptions[index % descriptions.length],
    icon: icons[index % icons.length],
    prompt: prompts[index % prompts.length]
  }));
};

