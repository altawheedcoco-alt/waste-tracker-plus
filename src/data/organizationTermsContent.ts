export const getAgreementText = (organizationType: OrganizationType): string => {
  const baseText = 'أقر بأنني قرأت وفهمت جميع الشروط والأحكام والسياسات الواردة أعلاه، وأوافق على الالتزام بها التزاماً كاملاً، وأتحمل كامل المسؤولية القانونية عن أي مخالفة. كما أوافق على إعفاء منصة آي ريسايكل من أي مسؤولية قانونية أو مالية ناتجة عن';
  
  switch (organizationType) {
    case 'generator':
      return `${baseText} عملياتي كجهة مولدة للمخلفات، بما يشمل التصنيف والتخزين والتسليم.`;
    case 'transporter':
      return `${baseText} عمليات النقل التي أقوم بها، بما يشمل المسار والتوثيق المرئي والالتزام بقواعد السلوك.`;
    case 'recycler':
      return `${baseText} عمليات التدوير والمعالجة التي أقوم بها، بما يشمل معايير القبول وإصدار الشهادات.`;
    case 'disposal':
      return `${baseText} عمليات التخلص النهائي التي أقوم بها.`;
    default:
      return `${baseText} عملياتي.`;
  }
};
