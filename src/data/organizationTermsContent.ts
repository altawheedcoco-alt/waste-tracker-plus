export const getTermsSections = (organizationType: OrganizationType): TermsSection[] => {
  switch (organizationType) {
    case 'generator':
      return generatorTermsSections;
    case 'transporter':
      return transporterTermsSections;
    case 'recycler':
      return recyclerTermsSections;
    case 'disposal':
      return disposalTermsSections;
    default:
      return transporterTermsSections;
  }
};

export const getOrganizationTypeLabel = (organizationType: OrganizationType): string => {
  switch (organizationType) {
    case 'generator':
      return 'جهة مولدة للمخلفات';
    case 'transporter':
      return 'جهة ناقلة للمخلفات';
    case 'recycler':
      return 'جهة مدورة للمخلفات';
    case 'disposal':
      return 'جهة تخلص نهائي';
    default:
      return 'جهة';
  }
};

