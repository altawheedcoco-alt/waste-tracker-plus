const generateContractTerms = (
  categoryName: string,
  isHazardous: boolean,
  partnerType: 'generator' | 'recycler',
  subcategoryName?: string
): string => {
  const wasteLabel = subcategoryName || categoryName;
  const hazardTag = isHazardous ? ' ⚠️ (مخلفات خطرة - تخضع لاشتراطات خاصة)' : '';

  if (partnerType === 'generator') {
    return generateGeneratorTransporterTerms(wasteLabel, isHazardous, hazardTag);
  }
  return generateTransporterRecyclerTerms(wasteLabel, isHazardous, hazardTag);
};

