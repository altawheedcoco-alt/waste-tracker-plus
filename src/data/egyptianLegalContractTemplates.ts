// دالة توليد رمز التحقق الفريد
export const generateContractVerificationCode = (): string => {
  const timestamp = Date.now().toString(36).toUpperCase();
  const random = Math.random().toString(36).substring(2, 8).toUpperCase();
  const checksum = (parseInt(timestamp, 36) % 97).toString().padStart(2, '0');
  return `EG-WMRA-${timestamp}-${random}-${checksum}`;
};

// دالة التحقق من صحة رمز العقد
export const validateContractCode = (code: string): boolean => {
  const pattern = /^EG-WMRA-[A-Z0-9]+-[A-Z0-9]+-\d{2}$/;
  if (!pattern.test(code)) return false;
  
  const parts = code.split('-');
  const timestamp = parts[2];
  const checksum = parseInt(parts[4]);
  const expectedChecksum = parseInt(timestamp, 36) % 97;
  
  return checksum === expectedChecksum;
};
