/**
 * Returns a focused subset of the knowledge base based on document type
 */
export function getRelevantKnowledge(documentType?: string): string {
  const base = LEGAL_FRAMEWORK + '\n\n' + INDUSTRY_GLOSSARY;

  switch (documentType) {
    case 'quotation':
      return base + '\n\n' + QUOTATION_TEMPLATES + '\n\n' + FINANCIAL_TEMPLATES;
    case 'proforma':
      return base + '\n\n' + FINANCIAL_TEMPLATES + '\n\n' + QUOTATION_TEMPLATES;
    case 'statement':
      return base + '\n\n' + FINANCIAL_TEMPLATES;
    case 'contract':
    case 'addendum':
      return base + '\n\n' + CONTRACT_TEMPLATES + '\n\n' + SLA_TEMPLATES;
    case 'sla':
      return base + '\n\n' + SLA_TEMPLATES + '\n\n' + CONTRACT_TEMPLATES;
    case 'letter':
    case 'guarantee':
      return base + '\n\n' + LETTER_TEMPLATES;
    case 'report':
    case 'safety_report':
    case 'environmental':
      return base + '\n\n' + REPORT_TEMPLATES;
    case 'work_order':
    case 'bill_of_lading':
    case 'hazmat_permit':
      return base + '\n\n' + OPERATIONAL_TEMPLATES;
    case 'maintenance':
      return base + '\n\n' + OPERATIONAL_TEMPLATES + '\n\n' + POLICY_TEMPLATES;
    case 'policy':
    case 'memo':
      return base + '\n\n' + POLICY_TEMPLATES;
    case 'certificate':
      return base + '\n\n' + OPERATIONAL_TEMPLATES;
    default:
      return getFullKnowledgeBase();
  }
}
